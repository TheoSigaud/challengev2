import Head from 'next/head'
import React, {useEffect, useState} from "react";
import HomeLayout from "@/components/layouts/Home";
import {router} from "next/client";
import {useRouter} from "next/router";

interface Announcement {
    name: string;
    description: string;
    firstImage: string;
    base64Images: string[];
}


export default function New() {
    const [announcement, setAnnouncement] = useState<Announcement[]>([]);
    const router = useRouter();

    useEffect(() => {
        document.body.classList.add("bg-custom-light-orange");
    });

    useEffect(() => {
        if(!router.isReady) return;

        fetch(`http://localhost:3001/announcement/${router.query.id}`, {
            method: 'GET',
        })
            .then(response => response.json())
            .then((data) => {
                setAnnouncement(data)
                console.log(data);
            }).catch((error) => {
            console.log(error);

        });
    }, [router.isReady]);

    return (
        <>
            <Head>
                <title>Ludotter</title>
                <meta name="description" content="Generated by create next app"/>
                <meta name="viewport" content="width=device-width, initial-scale=1"/>
                <link rel="icon" href="/favicon.ico"/>
            </Head>
            <HomeLayout>
                <section>
                    <div className="container mx-auto">
                        <div className="flex">
                        <div className="">
                            <div>
                                <img className="h-auto w-80 rounded-lg"
                                     src={announcement[0].base64Images[0]} alt="" />
                            </div>
                            <div className="">

                                {announcement[0].base64Images.slice(1).map((image, index) => (
                                    <div key={index}>
                                        <img className="h-auto w-80 rounded-lg" src={image} alt="" />
                                    </div>
                                ))}
                            </div>
                        </div>
                            <h4>{announcement[0].name}</h4>
                            <p>{announcement[0].description}</p>
                    </div>
                    </div>
                </section>
            </HomeLayout>
        </>
    )
}